/**
 * UK Payment Heatmap — Data Configuration
 * Region data and layer definitions for the interactive heatmap.
 * All intensity values are 0–1 normalised floats.
 */

export interface Region {
  name: string;
  lat: number;
  lng: number;
  contactless: number;
  digital_wallet: number;
  cash_ratio: number;
  tx_volume: number;
  merchant: number;
  mortgage: number;
  credit: number;
  open_banking: number;
}

export type LayerKey = Exclude<keyof Region, 'name' | 'lat' | 'lng'>;

export interface LayerConfig {
  key: LayerKey;
  /** Short label for the select dropdown */
  dropdownLabel: string;
  /** Full descriptive label shown as card subtitle */
  label: string;
  fmt: (v: number) => string;
  /** Leaflet.heat gradient stops */
  gradient: Record<string, string>;
  /** If true, intensity is inverted before rendering (e.g. cash = low card = hot) */
  invert?: boolean;
}

